     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $complaints = Complaint::where('status', 'diterima')->orWhere('status', 'diproses')->latest();

        if ($request->search) {
            $complaints = Complaint::where('title', 'like', '%'.$request->search.'%');
        }

        $complaints = $complaints->paginate(10);

        return view('admin.confirmation.index', compact('complaints'));
    }

